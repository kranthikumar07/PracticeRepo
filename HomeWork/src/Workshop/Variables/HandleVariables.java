package Workshop.Variables;

public class HandleVariables {
    public static void main(String[] args) {
        //E.g.
        int a = 2;
        long b = 1000000000000000000l;
        float c = 2.5f;
        double d = 100000000000000000000000000000000000000.5d;
        char e = 'a';
        String f = "Good";
        boolean g = true;

        System.out.println("My Integer Value = " + a);
        System.out.println("My Long Value = " + b);
        System.out.println("My Float Value = " + c);
        System.out.println("My Double Value = " + d);
        System.out.println("My Character Value = " + e);
        System.out.println("My String Value = " + f);
        System.out.println("My Boolean Value = " + g);

        //E.g.
        int x = 1;
        x = 2;
        System.out.println(x);

        //E.g.
        int y;
        y = 3;
        System.out.println(y);

        //E.g.
        int p = 4, q = 5, r = 6;
        System.out.println("My p value = " + p);
        System.out.println("My q value = " + q);
        System.out.println("My r value = " + r);



    }
}
