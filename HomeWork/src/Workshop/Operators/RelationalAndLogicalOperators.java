package Workshop.Operators;

public class RelationalAndLogicalOperators {
    public static void main(String[] args) {
        //AND
        int a =10, b =20;
        if((a==10) && (b==20)){
            System.out.println("true");
        }
        else{
            System.out.println("false");
        }

        //OR
        int c = 10, d = 20;

        if((c==10) || (d==30)){
            System.out.println("true");
        }
        else{
            System.out.println("false");
        }
    }
}
